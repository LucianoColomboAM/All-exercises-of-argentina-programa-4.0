function suma (numero_1, numero_2) {
	const total=numero_1 + numero_2;
	return total;
}

function resta (numero_1, numero_2) {
	const total=numero_1 - numero_2;
	return total;
}

function division (numero_1, numero_2) {
	const total=numero_1 / numero_2;
	return total;
}

function multiplicacion (numero_1, numero_2) {
	const total=numero_1 * numero_2;
	return total;
}